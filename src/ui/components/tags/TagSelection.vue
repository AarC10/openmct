<template>
<div class="c-tag_selection">
    <AutoCompleteField
        v-if="newTag"
        ref="tagSelection"
        :model="availableTagModel"
        :place-holder-text="'Type to add tag'"
        @onChange="tagSelected"
    />
    <div
        v-else
        class="c-completed-tag"
        :style="{ background: selectedBackgroundColor, color: selectedForegroundColor}"
    >
        <div class="c-completed-tag-label">{{ selectedTagLabel }} </div>
        <button
            class="c-completed-tag-deletion"
            :style="{ background: selectedBackgroundColor, color: selectedForegroundColor, opacity: 0.5}"
            @click="removeTag"
        >
            <b>x</b></button>
    </div>
</div>
</template>

<script>

import AutoCompleteField from '../../../api/forms/components/controls/AutoCompleteField.vue';

export default {
    components: {
        AutoCompleteField
    },
    inject: ['openmct'],
    props: {
        annotation: {
            type: Object,
            default() {
                return {};
            }
        },
        selectedTag: {
            type: String,
            default() {
                return "";
            }
        },
        entry: {
            type: Object,
            default() {
                return {};
            }
        },
        newTag: {
            type: Boolean,
            default() {
                return false;
            }
        }
    },
    data() {
        return {
        };
    },
    computed: {
        availableTagModel() {
            const availableTags = this.openmct.annotation.getAvailableTags().map(tag => {
                return {
                    name: tag.label.toUpperCase(),
                    color: tag.backgroundColor,
                    id: tag.id
                };
            });

            return {
                options: availableTags
            };
        },
        selectedBackgroundColor() {
            return this.getAvailableTagByID(this.selectedTag).backgroundColor;
        },
        selectedForegroundColor() {
            return this.getAvailableTagByID(this.selectedTag).foregroundColor;
        },
        selectedTagLabel() {
            return this.getAvailableTagByID(this.selectedTag).label.toUpperCase();
        }
    },
    mounted() {
    },
    methods: {
        getAvailableTagByID(tagID) {
            return this.openmct.annotation.getAvailableTags().find(tag => {
                return tag.id === tagID;
            });
        },
        removeTag() {
            this.$emit('tagRemoved', {
                entry: this.entry,
                tag: this.selectedTag
            });
        },
        tagSelected(autoField) {
            const tagAdded = autoField.model.options.find(option => {
                if (option.name === autoField.value) {
                    return true;
                }

                return false;
            });
            if (tagAdded) {
                this.$emit('tagAdded', {
                    entry: this.entry,
                    newTag: tagAdded.id
                });
            }
        }
    }
};
</script>
